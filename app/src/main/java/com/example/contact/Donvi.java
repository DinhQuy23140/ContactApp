package com.example.contact;

public class Donvi {
    private String madonvi, tendonvi, email, website, diachi, sdt, madonvicha, logo;

    public Donvi(String madonvi, String tendonvi, String email, String website, String diachi, String sdt, String madonvicha, String logo) {
        this.madonvi = madonvi;
        this.tendonvi = tendonvi;
        this.email = email;
        this.website = website;
        this.diachi = diachi;
        this.sdt = sdt;
        this.madonvicha = madonvicha;
        this.logo = logo;
    }

    public String getMadonvi() {
        return madonvi;
    }

    public void setMadonvi(String madonvi) {
        this.madonvi = madonvi;
    }

    public String getTendonvi() {
        return tendonvi;
    }

    public void setTendonvi(String tendonvi) {
        this.tendonvi = tendonvi;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }

    public String getDiachi() {
        return diachi;
    }

    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }

    public String getSdt() {
        return sdt;
    }

    public void setSdt(String sdt) {
        this.sdt = sdt;
    }

    public String getMadonvicha() {
        return madonvicha;
    }

    public void setMadonvicha(String madonvicha) {
        this.madonvicha = madonvicha;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }
}
